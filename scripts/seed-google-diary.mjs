const SOURCE = 'Дневник_трат_Google_Sheets'

const operations = rows.map(([period, category, amount]) => {
  const [yearStr, monthStr] = period.split('-')
  return {
    year: Number(yearStr),
    month: Number(monthStr),
    operationCategory: SOURCE,
    description: category,
    category,
    amount,
  }
})

const payload = {
  fileName: SOURCE,
  operations,
}

const response = await fetch(`${baseUrl}/api/operations/import`, {
  method: 'POST',
  headers: { 'Content-Type': 'application/json; charset=utf-8' },
  body: JSON.stringify(payload),
})

const body = await response.json()

if (!response.ok) {
  console.error('Import failed:', body.message ?? body)
  process.exit(1)
}

console.log(`Imported ${operations.length} operations into ${baseUrl}`)
console.log(`Total grouped rows on home page: ${body.operations.length}`)

